# Вторая версия - тренировка скорости печати с уровнями сложности
import time

print("=" * 40)
print("ТРЕНАЖЁР СКОРОСТИ ПЕЧАТИ v2.0")
print("=" * 40)

print("\nВыберите уровень сложности:")
print("1 - Лёгкий (короткая фраза)")
print("2 - Средний (предложение)")
print("3 - Сложный (длинный текст)")

choice = input("\nВаш выбор (1/2/3): ")

if choice == "1":
    text = "кот дом лес солнце"
    level_name = "Лёгкий"
elif choice == "2":
    text = "программирование это интересно и полезно"
    level_name = "Средний"
else:
    text = "разработка программного обеспечения требует внимания усидчивости и постоянной практики"
    level_name = "Сложный"

print(f"\nУровень: {level_name}")
print("\nНапечатайте этот текст:")
print(f"\n>>> {text} <<<\n")

input("Нажмите Enter, когда будете готовы...")

start = time.time()
user_input = input("Ваш ввод: ")
end = time.time()

if user_input == text:
    print(f"\n✅ Отлично! Ваше время: {end - start:.2f} секунд")
else:
    print("\n❌ Ошибка! Текст не совпадает. Попробуйте ещё раз.")
